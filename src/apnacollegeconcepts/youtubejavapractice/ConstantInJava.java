package youtubeJavaPractice;

public class ConstantInJava {

    public static void main(String[] args) {
        //constants
        int age = 30;
        age = 31;
        age = 32;
        System.out.println(age);

        final float PI = 3.14F;
        //We can't change the PI value so that we are adding Final keyword.
        // We give uppercase for variables.
        System.out.println(PI);
    }
}
